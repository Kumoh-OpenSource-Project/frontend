class PlacePostEntity {
  String title;
  String content;
  String nickName;
  String writeDate;
  String level;
  int likes;
  int clips;
  int comments;

  PlacePostEntity({
    required this.title,
    required this.content,
    required this.nickName,
    required this.writeDate,
    required this.level,
    required this.likes,
    required this.clips,
    required this.comments,
  });
}


